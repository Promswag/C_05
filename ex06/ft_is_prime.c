int	ft_is_prime(int nb)
{
	int	i;
	int	r;

	r = 1;
	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
			r++;
		i++;
	}
	if (r == 1)
		return (1);
	return (0);
}
