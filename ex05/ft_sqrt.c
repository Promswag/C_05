int	ft_sqrt(int nb)
{	
	int	i;
	int	q;
	int	r;

	if (nb == 1)
		return (1);
	i = 1;
	while (i < nb)
	{
		q = nb / i;
		r = nb % i;
		if (q == i && r == 0)
			return (q);
		if (q < i)
			return (0);
		i++;
	}
	return (0);
}
