int	ft_find_next_prime(int nb)
{
	int	i;
	int	r;

	while (1)
	{
		r = 1;
		i = 2;
		nb++;
		while (i < nb)
		{
			if (nb % i == 0)
				r++;
			i++;
		}
		if (r == 1)
			return (nb);
	}
}
